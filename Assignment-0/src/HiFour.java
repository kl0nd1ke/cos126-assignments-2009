public class HiFour {
    public static void main(String[] args) {
        System.out.print("Hi ");
        System.out.print(args[3] + ", ");
        System.out.print(args[2] + ", ");
        System.out.print(args[1] + ", and ");
        System.out.print(args[0] + ".");
    }

}
